public class Operacionesbooleanas {

    public static void main(String[] args) {

        System.out.println("En la tabla de AND los resultados son así:");
        System.out.println(" ");

        System.out.print("V y V es ");
        System.out.println(true&&true);

        System.out.print("V y F es ");
        System.out.println(true&&false);

        System.out.print("F y V es ");
        System.out.println(false&&true);

        System.out.print("F y F es ");
        System.out.println(false&&false);
        System.out.println(" ");

        System.out.println("En la tabla de OR los resultados son así:");
        System.out.println(" ");

        System.out.print("V y V es ");
        System.out.println(true||true);

        System.out.print("V y F es ");
        System.out.println(true||false);

        System.out.print("F y V es ");
        System.out.println(false||true);

        System.out.print("F y F es ");
        System.out.println(false||false);
        System.out.println(" ");



    }

}
